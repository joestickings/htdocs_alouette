        <h1>Heathrow Zone Transit - A Day trip to Oxford</h1>
        <p>Flying is one of those things, which is very addictive and since the CAA gave me a license in early December I have managed 10 more hours experience in my logbook, having made about 6 transits across Gatwick sharing the flying dream with my friends and fellow pilots.</p>
        <img src="images/reports/oxford-via-heathrow/1.jpg" width="392" height="294" alt="Gatwick Overhead" class="rightImage" />
        <p>So having gained some experience in the clubs Cessna 172, I decided it was time for a good checkout on the club’s Piper Warrior. Instructor Ian Davidson got me up to speed with things but unfortunately the next two weeks were some of the coldest days we've had since the December 2010 big freeze, and that meant some very icy days, or as Bruno and I call them "stay home and put the kettle on days".</p>
        <p>I decided it was time for something further afield than the far South East of England, where all of my training with Bruno and Ian took place. I invited my friends, which funny enough were both qualified pilots; Joris (an ATPL First Officer for Luxair, who qualified at Oxford Aviation Academy) and Amie (who raced me to the finish and got her PPL at Shoreham Airport a month before I did) so at least I was being well looked after on the adventure we were about to embark on.</p>
        <p>Lucky enough the weather looked fantastic, and a day trip to Oxford Kidlington airport was about to be a recorded in my logbook. However, something was preventing a direct flight to Oxford.</p>
        <p>Oh yes Heathrow, one of the world's - and the UK’s - busiest international airport!</p>
        <p>The airspace in which it is situated is not available for VFR, however those on a basic 'vanilla' PPL can fly through the zone Special VFR so long as the visibility is greater than 10km and they are clear of cloud and in sight of the surface. The AIP actually specifically says: "Prior notification of Special VFR flights or general enquiries about SVFR flights in the London CTR may be made via the LTCC Senior Watch Assistant" along with the phone number (which is best obtained from the AIP in case it changes!) so it made me wonder, can you really go through this airspace???</p>
        <p>So I made a call to the Senior Watch Assistant to see if it would be possible and to my surprise, IT WAS!</p>
        <p>OCK VOR on-track WOODLEY NDB at 1500ft on the London QNH.</p>
        <p>Leaving London and Heathrow in the distance, we climbed towards 3,300ft but didn’t made it due to some low cloud and levelled off at 2,900ft south of Benson MATZ. As we travelled towards Oxford we had to transit the MATZ, which I've never done before but it was fairly straightforward and a fantastic MATZ and ATZ penetration ensued, which topped off a fantastic flight with a nice descent into sunny Oxford.</p>
        <p>As we approached Oxford, it was noticeably quieter than we expected and so a straight in approach was offered remaining clear of Brize Norton's airspace of course.</p>
        <p>Having enjoyed a spot of lunch with Joris and Amie at Oxford and a fantastic tour of the Aviation Academy, we decided to make a return flight back to Biggin, so we gave Heathrow another call and they wanted us to route ever closer to the Airport on the way back, which was BUR NDB to ASCOT racecourse. But as we got nearer to the airport, they had helicopter traffic which did not permit us into the airspace, as you have to be 3 miles apart in Class A airspace.</p>
        <img src="images/reports/oxford-via-heathrow/3.jpg" width="184" height="245" alt="Pilots" class="rightImage" />
        <p>So we routed back towards Woodley with the expectation that Heathrow would send us through Farnborough, but no they wanted us even closer to Heathrow Airport (3.8 nautical miles to be exact) routing WOODLEY NDB > ASCOT > THORPE PARK and then following the M25 out of the Zone. Interesting scenarios, because there were a lot of passenger jets taking off in that direction and we managed to get very close to a few departures, although they probably appeared closer than they actually where.</p>
        <p>As we got to Thorpe Park, a 747 crossed over us at 1000ft and we could clearly see Heathrow in the distance and wondered why on earth they wanted us this close, maybe it was an early Birthday present or they trusted me not to muck up inside the Zone and get lost.</p>
        <p>Fantastic day out and it was a shame that it ended so quickly as we approached Biggin Hill Airport for a Sunset landing.</p>
        <p>A rather enjoyable trip and 2hrs 15 minutes in my logbook!</p>
        <p>Robbie Garrett</p>

