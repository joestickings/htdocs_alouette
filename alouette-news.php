                <p>You may have noticed that there is a big sporting event going on in London at the moment and this has affected our ability to book an aircraft and go flying, almost at the drop of a hat. It is now necessary to file a flight plan and obtain permission from Atlas Control and you can find more details at <a href="http://olympics.airspacesafety.com/" target="_blank">http://olympics.airspacesafety.com/</a></p>
