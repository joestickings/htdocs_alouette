            <li>
                <h2>Should I choose an NPPL or JAA PPL course?</h2>
                <p>This depends on where you want to fly, what type of flying you will want to do and, of course, your budget. Basically the NPPL is a licence which requires a minimum of 32 hours flight training but allows you to only fly within the UK. It cannot be extended to add, for instance, a night rating . By contrast the JAA licence requires a minimum of 45 hours flight training, is an international licence and can be further extended, if required. The medical requirements are also different, the NPPL only requires a medical declaration form signed by your GP whereas the JAA NPPL requires a Class 2 medical. Alouette provide training for both the NPPL and JAA PPL licences.</p>
            </li>
            <li>
                <h2>Why is Alouette's flying rate cheaper than anyone else?</h2>
                <p>Alouette Flying Club is a non profit making organisation that is run by it's members, we do not have any payroll staff. The more flying hours that our aircraft do, the cheaper our flying rate becomes.</p>
            </li>
            <li>
                <h2>How much will it actually cost to get a JAA Private Pilot Licence?</h2>
                <p>You may have seen advertisements in the aviation press and other flying clubs, a fixed price to get your PPL. Be aware, these prices are based on 45 hours flying training. This is minimum period of time that a student must accomplish to satisfy the Civil Aviation Authority before a license is granted, however the average period is about 55 hours. To learn to fly at Alouette there are the following charges: The hourly rental of the aircraft, the instructors fee of £30 per hour, payable to the instructor, the landing fee £22.00, the annual club membership fee £150 per year. In addition, there are exams and licence fees. You do not pay in advance, you pay as you go, therefore you learn at your own pace. If your training or budget needs to be spread out over a longer period of time , that's OK. Fixed price training requires you to complete your 45 hours within a set time scale, at Alouette you can take as long as you like.</p>
            </li>
            <li>
                <h2>What benefits do I get from Alouette as a member?</h2>
                <p>As a student you will receive first class training in good quality aircraft. Students have free use of the clubs headsets. As a member of Alouette you are expected to participate in the running of the club in any way you can, whether it be contributing some time washing the aircraft, cutting the lawn, tidying up the club house, it's all important. You are expected to take an active part in the social side of the club, we are a flying club not just a flying school. In return you will benefit from the cheapest flying rate in the south of England. The club has a very strong social element, regular fly-aways occur throughout the year. The first Wednesday evening of each month, members meet at the club house for a social evening. Students are encouraged to come along to meet other students and club members. It is a very relaxed and welcoming atmosphere.</p>
            </li>
            <li>
                <h2>Where can I fly once I get my JAA PPL?</h2>
                <p>Your PPL will allow you to fly almost anywhere in the world. It is recognised internationally. Our Cessna 172 has flown to Morocco, through France, Spain and Gibraltar. By contrast the NPPL (see above) only allows you to fly within the UK. </p>
            </li>
            <li>
                <h2>Are the Aircraft well maintained?</h2>
                <p>We lease our aircraft from a company based on Biggin Hill Aerodrome. They lease aircraft to many other flying clubs and regularly maintain the aircraft to the very high CAA standard.</p>
            </li>
            <li>
                <h2>What should I do next?</h2>
                <p>Contact our Chairman or Secretary to discuss it further or pop along to the club house on a Wednesday evening 8:30 pm onwards or come and visit the club on a Saturday or Sunday, there will usually be somebody about to chat to. You can take a <a href="/trial-flights" title="Trial lesson">trial lesson</a> just to see if you're comfortable at the controls of a light aircraft. During the lesson you will be given the opportunity to fly the aircraft yourself under the watchful eye of your instructor. Any time that you spend during this trial flight can be logged and can go towards your 32 or 45 hours flying training.</p>
            </li>
            <li>
                <h2>What is the cost of Social Membership and what benefits are there?</h2>
